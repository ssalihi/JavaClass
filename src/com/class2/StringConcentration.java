package com.class2;

public class StringConcentration {
	public static void main(String[] args) {
		String name = " john";
		String lastName = "smith";
		char grade = 'A';

		// Print:
		// My name is John and my last name is Smith.
		// I am A student.
		// I live in Woodbridge in state of VA.
		// My phone number is 4637828
		System.out.println("My name is" + name + " and my last name is " + lastName + ".");
		String city = "Woodbridge";
		System.out.println("I live in " + city + " in state of VA.");
		System.out.println("I am a grade " + grade + " student.");
		String phonenumber = "5712447013";
		System.out.println("My phone number is " + phonenumber + ".");

	}

}
