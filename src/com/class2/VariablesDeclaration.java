package com.class2;

public class VariablesDeclaration {
	public static void main(String[] args) {
		// different ways to create/declare variables;
		// 1. create variable and assign value
		int num = 28;

		// 2 ways: using 2 steps
		int y; // 1. declaring variable
		y = 12; // 2. assign value
		// declare 1 by 1
		int a;
		int b;
		int c;
		// or declare all together
		int d, e, f, g;
		a = 1;
		b = 2;
		c = 3;
		d = 4;
		e = 5;
		f = 6;
		g = 7;

		// for windows ctrl_shift+f= formating
		

	}
}
