package com.Class23;

public class MainMethodOverload {
public static void main(String[] args) {
	System.out.println("I am a main method with string of array");
}
public static void main () {// 
	System.out.println("I am a main method with no parameters");
}
public static void main(String args) {
	System.out.println("I m a main method with string parameter");
}
public static void main(int a) {
	System.out.println("I m a main method with int parameter");
}
}
